import { ActiveCatalogModel, AvailableVia, DataQuality, LifecycleStatus, PrimaryModality } from "../types";

type SeedModel = {
  id: string;
  vendorId: string;
  vendor: string;
  modelId: string;
  modelName: string;
  primaryModality: PrimaryModality;
  capabilities: string[];
  inputTypes: string[];
  outputTypes: string[];
  contextWindow?: string | number;
  availableVia: AvailableVia[];
  lifecycleStatus?: LifecycleStatus;
  dataQuality?: DataQuality;
  pricingStatus?: "official_found" | "official_not_found" | "ambiguous" | "not_applicable";
  pricingInput?: string;
  pricingOutput?: string;
  pricingDetails?: string;
  officialSourceUrls: string[];
  pricingSourceUrl?: string;
  firstSeenAt?: string;
  officialLaunchDate?: string;
  firstAvailableDate?: string;
  lastVerifiedAt?: string;
  summary: string;
  notes?: string;
};

function seed(model: SeedModel): ActiveCatalogModel {
  return {
    id: model.id,
    vendorId: model.vendorId,
    vendorName: model.vendor,
    modelId: model.modelId,
    displayName: model.modelName,
    aliases: [],
    status: model.lifecycleStatus === "deprecated" ? "deprecated" : "active",
    lifecycleStatus: model.lifecycleStatus || "source_verified",
    dataQuality: model.dataQuality || (model.pricingStatus === "official_found" ? "verified" : "pricing_unverified"),
    availableVia: model.availableVia,
    availability: "ga",
    primaryModality: model.primaryModality,
    capabilities: model.capabilities,
    inputTypes: model.inputTypes,
    outputTypes: model.outputTypes,
    contextWindow: model.contextWindow || "N/A",
    pricing: {
      pricingModel: model.pricingStatus === "not_applicable" ? "free" : "unknown",
      input1m: model.pricingInput || "N/A",
      output1m: model.pricingOutput || "N/A",
      pricingStatus: model.pricingStatus || "official_not_found",
      pricingSourceUrl: model.pricingSourceUrl,
      pricingDetails: model.pricingDetails,
    },
    officialSourceUrls: model.officialSourceUrls,
    pricingSourceUrl: model.pricingSourceUrl,
    firstSeenSourceUrl: model.officialSourceUrls[0] || "",
    firstSeenAt: model.firstSeenAt || model.officialLaunchDate || model.firstAvailableDate || "2026-06-25T00:00:00Z",
    officialLaunchDate: model.officialLaunchDate,
    firstAvailableDate: model.firstAvailableDate,
    lastSeenAt: model.lastVerifiedAt || "2026-06-25T00:00:00Z",
    lastVerifiedAt: model.lastVerifiedAt || "2026-06-25T00:00:00Z",
    summaryZh: model.summary,
    notes: model.notes || "",
    confidence: model.dataQuality === "needs_review" ? "medium" : "high",
    reviewStatus: model.dataQuality === "needs_review" ? "needs_review" : "auto_verified",
    sourceSnapshotIds: [],
    gapFlags: [],
    modelName: model.modelName,
    vendor: model.vendor,
    releaseDate: model.officialLaunchDate || model.firstAvailableDate || model.firstSeenAt || "2026-06-25",
    category:
      model.primaryModality === "image" || model.primaryModality === "video" || model.primaryModality === "vision"
        ? "vision"
        : model.primaryModality === "audio" || model.primaryModality === "tts" || model.primaryModality === "asr"
          ? "audio"
          : model.availableVia.includes("open_weights")
            ? "open-source"
            : "general",
    pricingInput: model.pricingInput || "N/A",
    pricingOutput: model.pricingOutput || "N/A",
    keyFeatures: model.capabilities,
  };
}

